import { readdirSync, existsSync, readFileSync, writeFileSync } from "node:fs";
import path from "node:path";
import type { Config } from "@docusaurus/types";
import type { Options as PresetOptions } from "@docusaurus/preset-classic";
import type { Options as DocsOptions } from "@docusaurus/plugin-content-docs";
import { createHighlighter } from "shiki";
import { SHARED_COLOR_MODE, NAVBAR_ICON_ITEMS } from "./framework/shared-config.js";
import type { SidebarItem } from "./framework/config/schema.js";
import { convertSidebar } from "./framework/config/sidebar.js";
import { createAssetsPlugin } from "./framework/config/assets-plugin.js";
import { createRehypeShiki } from "./rehype-shiki.js";

const SITE_URL = "https://docs.trickfirerobotics.com";
const CONTENT_DIR = path.join(process.cwd(), "content");
const DEV_FIXTURES_DIR = path.join(process.cwd(), "dev-fixtures");
const IS_DEV = process.env.NODE_ENV !== "production";

function getFirstDocId(sidebar: SidebarItem[]): string | undefined {
    for (const item of sidebar) {
        if ("items" in item) {
            const first = getFirstDocId(item.items);
            if (first) return first;
        } else if (item.slug) {
            return item.slug;
        }
    }
    return undefined;
}

function makeRedirectHtml(url: string): string {
    return (
        `<!doctype html><html lang="en"><head><meta charset="UTF-8">` +
        `<title>Redirecting…</title>` +
        `<meta http-equiv="refresh" content="0; url=${url}">` +
        `<script>window.location.replace(${JSON.stringify(url)})</script></head>` +
        `<body><p>Redirecting to <a href="${url}">${url}</a>…</p></body></html>\n`
    );
}

interface RepoMeta {
    id: string;
    name: string;
    description: string;
    basePath: string;
    sidebarPath?: string;
    firstDocId?: string;
}

function getReposFromDir(baseDir: string): RepoMeta[] {
    if (!existsSync(baseDir)) return [];
    return readdirSync(baseDir, { withFileTypes: true })
        .filter((d) => d.isDirectory())
        .map((d) => d.name)
        .sort()
        .map((repo) => {
            const repoDir = path.join(baseDir, repo);
            try {
                const cfgPath = path.join(repoDir, "docs.config.json");
                const cfg = JSON.parse(readFileSync(cfgPath, "utf-8")) as {
                    name?: string;
                    description?: string;
                    sidebar?: SidebarItem[];
                };

                let sidebarPath: string | undefined;
                let firstDocId: string | undefined;
                if (cfg.sidebar) {
                    const converted = convertSidebar(cfg.sidebar);
                    const generatedPath = path.join(repoDir, "sidebars.generated.js");
                    writeFileSync(
                        generatedPath,
                        `// @generated by trickfire-docs — do not edit\nexport default { sidebar: ${JSON.stringify(converted, null, 2)} };\n`
                    );
                    sidebarPath = generatedPath;
                    firstDocId = getFirstDocId(cfg.sidebar);
                }

                return {
                    id: repo,
                    name: cfg.name ?? repo,
                    description: cfg.description ?? "",
                    basePath: repoDir,
                    sidebarPath,
                    firstDocId,
                };
            } catch {
                return { id: repo, name: repo, description: "", basePath: repoDir };
            }
        });
}

const SHIKI_THEME = "github-dark";

export default async function createConfig(): Promise<Config> {
    const shikiHighlighter = await createHighlighter({
        themes: [SHIKI_THEME],
        langs: [
            "bash",
            "shell",
            "shellscript",
            "shellsession",
            "python",
            "typescript",
            "javascript",
            "json",
            "yaml",
            "nginx",
            "markdown",
            "c",
            "cpp",
            "text",
        ],
    });

    const rehypeShiki = createRehypeShiki(shikiHighlighter, SHIKI_THEME);

    const repoMeta: RepoMeta[] = [
        ...getReposFromDir(CONTENT_DIR),
        ...(IS_DEV ? getReposFromDir(DEV_FIXTURES_DIR) : []),
    ];

    const frameworkDocsPlugin: NonNullable<Config["plugins"]>[number] = [
        "@docusaurus/plugin-content-docs",
        {
            id: "framework",
            path: "docs",
            routeBasePath: "trickfire-docs",
            sidebarPath: "./docs-sidebars.js",
            rehypePlugins: [rehypeShiki],
        } satisfies DocsOptions,
    ];

    const docsPlugins: Config["plugins"] = repoMeta.map(({ id, basePath, sidebarPath }) => [
        "@docusaurus/plugin-content-docs",
        {
            id,
            path: basePath,
            routeBasePath: id,
            exclude: ["assets/**"],
            ...(sidebarPath && { sidebarPath }),
            rehypePlugins: [rehypeShiki],
        } satisfies DocsOptions,
    ]);

    const projectAssetsPlugin = createAssetsPlugin(
        repoMeta.map(({ id, basePath }) => ({
            assetsDir: path.join(basePath, "assets"),
            publicPath: `/${id}/assets`,
            outSubPath: `${id}/assets`,
        }))
    );

    const subprojectRedirectsPlugin: NonNullable<Config["plugins"]>[number] = () => ({
        name: "trickfire-subproject-redirects",
        postBuild({ outDir }: { outDir: string }) {
            for (const { id, firstDocId } of repoMeta) {
                const indexPath = path.join(outDir, id, "index.html");
                if (existsSync(indexPath)) continue;

                let target = firstDocId;
                if (!target) {
                    const repoOutDir = path.join(outDir, id);
                    if (!existsSync(repoOutDir)) continue;
                    target = readdirSync(repoOutDir, { withFileTypes: true })
                        .filter((d) => d.isDirectory())
                        .map((d) => d.name)
                        .sort()[0];
                }
                if (!target) continue;

                writeFileSync(indexPath, makeRedirectHtml(`/${id}/${target}/`));
            }
        },
    });

    const projectDropdownItems = repoMeta.map(({ id, name, firstDocId }) => ({
        label: name,
        ...(firstDocId ? { to: `/${id}/${firstDocId}/` } : { href: `/${id}/` }),
    }));

    return {
        title: "TrickFire Robotics Docs",
        tagline: "Documentation for TrickFire Robotics projects",
        favicon: "favicon.ico",
        url: SITE_URL,
        baseUrl: "/",
        trailingSlash: true,
        staticDirectories: ["public"],
        onBrokenLinks: "warn",
        onBrokenMarkdownLinks: "warn",
        customFields: {
            repoMeta: repoMeta.map(({ id, name, description, firstDocId }) => ({
                id,
                name,
                description,
                firstDocId,
            })),
            frameworkMeta: {
                name: "Docs Framework",
                description: "Documentation for the TrickFire Docs framework itself",
                routeBasePath: "trickfire-docs",
            },
        },
        future: {
            faster: {
                rspackBundler: true,
                rspackPersistentCache: true,
                swcJsLoader: true,
                swcJsMinimizer: true,
                swcHtmlMinimizer: true,
            },
        },
        themeConfig: {
            colorMode: SHARED_COLOR_MODE,
            navbar: {
                title: "",
                logo: {
                    alt: "TrickFire Robotics logo",
                    src: "nav-logo.png",
                    srcDark: "nav-logo.png",
                    width: 80,
                    height: 30,
                },
                items: [
                    {
                        to: "/trickfire-docs/",
                        label: "Docs Framework",
                        position: "left" as const,
                    },
                    ...projectDropdownItems.map((item) => ({
                        ...item,
                        position: "left" as const,
                    })),
                    ...NAVBAR_ICON_ITEMS,
                ],
            },
        },
        presets: [
            [
                "classic",
                {
                    docs: false,
                    blog: false,
                    theme: { customCss: ["./src/css/custom.css"] },
                } satisfies PresetOptions,
            ],
        ],
        plugins: [
            frameworkDocsPlugin,
            ...docsPlugins,
            subprojectRedirectsPlugin,
            projectAssetsPlugin,
        ],
    };
}
