import path from "node:path";
import { fileURLToPath } from "node:url";
import type { ResolvedDocsConfig } from "./resolve.js";
import { convertSidebar, getFirstDocSlug } from "./sidebar.js";

// This module is bundled into dist-cli/cli.js; single-project.js lives in the same directory.
const SINGLE_PROJECT_CONFIG_PATH = path.join(
    path.dirname(fileURLToPath(import.meta.url)),
    "single-project.js"
);

export interface GeneratedFiles {
    /** Content for .trickfire-docs/docusaurus.config.js */
    config: string;
    /** Content for .trickfire-docs/sidebars.js, or null to use autogenerated sidebar. */
    sidebars: string | null;
    /** Content for .trickfire-docs/pages/index.jsx, or null if no redirect is needed. */
    redirectPage: string | null;
}

export function generateFiles(config: ResolvedDocsConfig, projectRoot: string): GeneratedFiles {
    const trickfireDir = path.resolve(projectRoot, ".trickfire-docs");
    const sidebarsPath = config.sidebar ? path.join(trickfireDir, "sidebars.js") : null;

    let sidebars: string | null = null;
    if (config.sidebar) {
        const converted = convertSidebar(config.sidebar);
        sidebars = `// @generated by trickfire-docs — do not edit\nexport default { sidebar: ${JSON.stringify(converted, null, 2)} };\n`;
    }

    const firstDocSlug = config.sidebar ? getFirstDocSlug(config.sidebar) : null;

    let redirectPage: string | null = null;
    if (firstDocSlug) {
        redirectPage = [
            `// @generated by trickfire-docs — do not edit`,
            `import { Redirect } from "@docusaurus/router";`,
            `export default function Home() { return <Redirect to="/${firstDocSlug}/" />; }`,
        ].join("\n");
    }

    const options = {
        projectRoot,
        sidebarsPath,
        name: config.name,
        description: config.description ?? "",
        firstDocSlug,
    };

    const configJs = [
        `// @generated by trickfire-docs — do not edit`,
        `import { createSingleProjectConfig } from ${JSON.stringify(SINGLE_PROJECT_CONFIG_PATH)};`,
        `export default createSingleProjectConfig(${JSON.stringify(options, null, 2)});`,
    ].join("\n");

    return { config: configJs, sidebars, redirectPage };
}
